// Deklaration einer Variablen mit 'let' (Zahl)
let alter = 25;

// Deklaration einer Variablen mit 'const' (String)
const name = 'Max';

// Deklaration einer Variablen mit 'var' (Boolean)
var istStudent = true;

// Ausgabe der Werte in der Konsole
console.log('Name:', name);          // Ausgabe: Name: Max
console.log('Alter:', alter);        // Ausgabe: Alter: 25
console.log('Ist Student:', istStudent); // Ausgabe: Ist Student: true