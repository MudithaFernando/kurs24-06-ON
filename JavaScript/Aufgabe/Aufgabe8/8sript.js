// Funktion zur Begrüßung
function begruessung(name) {
    return `Hallo, ${name}!`;
}

// Testen der Funktion mit verschiedenen Namen
console.log(begruessung("Max"));    // Ausgabe: Hallo, Max!
console.log(begruessung("Anna"));    // Ausgabe: Hallo, Anna!
console.log(begruessung("Mudit"));   // Ausgabe: Hallo, Mudit!
console.log(begruessung("Laura"));   // Ausgabe: Hallo, Laura!